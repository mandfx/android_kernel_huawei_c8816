static void set_touch_probe_flag(int detected)
{
	if(detected >= 0)
	{
		atomic_set(&touch_detected_yet, 1);
	}
	else
	{
		atomic_set(&touch_detected_yet, 0);
	}
	return;
}
static int read_touch_probe_flag(void)
{
	return atomic_read(&touch_detected_yet);
}
